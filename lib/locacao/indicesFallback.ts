export function obterIndiceFallback(serie: number, dataInicio: string, dataFim: string): ValorIndice[] {
  const valores = VALORES[serie];
  if (!valores) return [];
  const inicio = new Date(`${dataInicio}T00:00:00Z`).getTime();
  const fim = new Date(`${dataFim}T23:59:59Z`).getTime();

  return valores.flatMap((valor, indice) => {
    const data = new Date(Date.UTC(2023, indice, 1));
    if (data.getTime() < inicio || data.getTime() > fim) return [];
    return [{
      data: data.toLocaleDateString("pt-BR", { timeZone: "UTC" }),
      valor: valor.toFixed(2),
    }];
  });
}
